var cur = 'en';

function detectLang() {
  var nav = (typeof navigator !== 'undefined') ? (navigator.language || navigator.userLanguage || 'en') : 'en';
  nav = nav.toLowerCase();
  if (LANGS[nav]) return nav;
  var base = nav.split('-')[0];
  if (base === 'zh') return (nav.indexOf('tw') >= 0 || nav.indexOf('hk') >= 0) ? 'zh-TW' : 'zh-CN';
  for (var k in LANGS) {
    if (k.toLowerCase().split('-')[0] === base) return k;
  }
  return 'en';
}

function t(k) {
  return (LANGS[cur] && LANGS[cur].d[k]) || (LANGS['en'] && LANGS['en'].d[k]) || k;
}

function fmt(k) {
  var s = t(k);
  for (var i = 1; i < arguments.length; i++) {
    s = s.replace('{' + (i - 1) + '}', arguments[i]);
  }
  return s;
}

function setLang(l) {
  if (LANGS[l]) cur = l;
}

function getCur() { return cur; }

function getList() {
  var r = [];
  for (var k in LANGS) {
    if (LANGS.hasOwnProperty(k)) r.push({ code: k, name: LANGS[k].name });
  }
  return r;
}

function apply() {
  if (typeof document === 'undefined') return;
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    /* special: tip banner - reconstruct safely using DOM API */
    var key = el.getAttribute('data-i18n-html');
    if (key === 'tipContent') {
      el.textContent = '';
      var strong = document.createElement('strong');
      strong.textContent = t('tipTitle');
      el.appendChild(strong);
      el.appendChild(document.createTextNode(t('tipText')));
      var a = document.createElement('a');
      a.id = 'openSettings';
      a.textContent = t('tipSiteSettings');
      el.appendChild(a);
      el.appendChild(document.createTextNode(t('tipSuffix')));
    }
  });
  document.documentElement.lang = cur;
}

function init(cb) {
  cur = detectLang();
  if (typeof chrome !== 'undefined' && chrome.storage && chrome.storage.local) {
    chrome.storage.local.get(['userLang'], function(res) {
      if (res && res.userLang && LANGS[res.userLang]) cur = res.userLang;
      if (cb) cb();
    });
  } else {
    if (cb) cb();
  }
}

function saveLang(lang) {
  if (typeof chrome !== 'undefined' && chrome.storage && chrome.storage.local) {
    chrome.storage.local.set({ userLang: lang });
  }
}

return {
  init: init, t: t, fmt: fmt, setLang: setLang,
  getCur: getCur, getList: getList, apply: apply,
  saveLang: saveLang, LANGS: LANGS, detectLang: detectLang
};

